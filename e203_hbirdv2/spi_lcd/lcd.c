//********************************************************************/
//Function: LCD D/C Control pin set to 1
//********************************************************************/
void LCD_RS_SET(void)
{
    gpio_write(GPIOA, LCD_RS_MASK, 1);
}


//********************************************************************/
//Function: LCD D/C Control pin set to 0
//********************************************************************/
void LCD_RS_CLR(void)
{
    gpio_write(GPIOA, LCD_RS_MASK, 0);
}


//********************************************************************/
//Function: LCD Reg set 
//********************************************************************/
void LCD_WR_REG(uint16_t regval)
{  
    uint32_t wdata = ((uint32_t) regval&0x00FF) << 24;
    
    LCD_RS_CLR();

    spi_set_datalen(SPI1, 8);
    spi_write_fifo(SPI1, &wdata, 8);
    spi_start_transaction(SPI1, SPI_CMD_WR, SPI_CSN0);
    while ((spi_get_status(SPI1) & 0xFFFF) != 1);
}


//*******************************************************************/
//Function: LCD Write Data(8-bit)
//*******************************************************************/
void LCD_WR_DATA8(uint8_t data)  
{
    uint32_t wdata = ((uint32_t) data) << 24;

    LCD_RS_SET();

    spi_set_datalen(SPI1, 8);
    spi_write_fifo(SPI1, &wdata, 8);
    spi_start_transaction(SPI1, SPI_CMD_WR, SPI_CSN0);
    while ((spi_get_status(SPI1) & 0xFFFF) != 1);
}


//*******************************************************************/
//Function: LCD Write Data(16-bit)
//*******************************************************************/
void LCD_WR_DATA16(uint16_t data)
{	
    uint32_t wdata = ((uint32_t) data) << 16;
	
    LCD_RS_SET();
	
    spi_set_datalen(SPI1, 16);
    spi_write_fifo(SPI1, &wdata, 16);
    spi_start_transaction(SPI1, SPI_CMD_WR, SPI_CSN0);
    while ((spi_get_status(SPI1) & 0xFFFF) != 1);
}



//*******************************************************************/
//Function: LCD Scan Direction setting
//*******************************************************************/

void LCD_Scan_Dir(uint8_t dir)
{
    uint16_t regval=0;

    switch(dir)
    {
        case L2R_U2D:
             regval|=(0<<7)|(0<<6)|(0<<5); 
             break;
        case L2R_D2U:
             regval|=(1<<7)|(0<<6)|(0<<5); 
             break;
        case R2L_U2D:
             regval|=(0<<7)|(1<<6)|(0<<5); 
             break;
        case R2L_D2U:
             regval|=(1<<7)|(1<<6)|(0<<5); 
             break;	 
	case U2D_L2R:
             regval|=(0<<7)|(0<<6)|(1<<5); 
             break;
        case U2D_R2L:
             regval|=(0<<7)|(1<<6)|(1<<5); 
             break;
        case D2U_L2R:
             regval|=(1<<7)|(0<<6)|(1<<5); 
             break;
        case D2U_R2L:
	     regval|=(1<<7)|(1<<6)|(1<<5); 
	     break;	 
    }
    		
    LCD_WriteReg(0x36, regval|0x08);
} 




